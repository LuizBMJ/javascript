
let num = []
c = 0

for (c; c <= 10; c++ ) {
    num.push(c)
    console.log(`============================================`)
    console.log(`Nosso vetor é ${num}`)
    console.log(`O tamanho do vetor é ${num.length} `)
    console.log(`O primeiro numero do vetor é ${num[0]}`)
    console.log(`O ultimo numero do vetor é ${num[c]}`)
    console.log(`============================================`)
}

